import math as m
def square(x):
    return x ** 2
def square_root(x):
    return m.sqrt(x)
def power(x,y):
    return x ** y
def ten_power(x):
    return 10 ** x
def log(x):
    return m.log(x)
def fact(x):
    return m.factorial(x)
def mod(x,y):
    return x % y
def modulus(x):
    return abs(x)
